#!/usr/bin/env node
// ─────────────────────────────────────────────────────────────────────────
// fetch-class-features.mjs  —  one-time data generator (run manually)
//
//   node scripts/fetch-class-features.mjs
//
// Pulls base-class features (by level, with descriptions) for all 12 core SRD
// classes from the dnd5e API and writes ../class-features.js, which defines a
// global `CLASS_FEATURES` object consumed by script.js.
//
// Subclass-specific features are intentionally NOT included — only base-class
// features keyed by the class level at which they're gained.
// ─────────────────────────────────────────────────────────────────────────

import { writeFile } from 'node:fs/promises';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const API = 'https://www.dnd5eapi.co/api/2014';
const CLASSES = [
  'barbarian', 'bard', 'cleric', 'druid', 'fighter', 'monk',
  'paladin', 'ranger', 'rogue', 'sorcerer', 'warlock', 'wizard',
];
// The 12 SRD subclasses (one per class). Tasha's/Xanathar's subclasses are not in
// the SRD — those live in the hand-authored homebrew-content.js.
const SUBCLASSES = [
  'berserker', 'champion', 'devotion', 'draconic', 'evocation', 'fiend',
  'hunter', 'land', 'life', 'lore', 'open-hand', 'thief',
];

const __dirname = dirname(fileURLToPath(import.meta.url));
const OUT = join(__dirname, '..', 'class-features.js');

async function getJSON(url) {
  const res = await fetch(url);
  if (!res.ok) throw new Error(`HTTP ${res.status} for ${url}`);
  return res.json();
}

// Simple in-run cache so shared features aren't fetched twice.
const featureCache = new Map();
async function getFeature(index) {
  if (featureCache.has(index)) return featureCache.get(index);
  const data = await getJSON(`${API}/features/${index}`);
  featureCache.set(index, data);
  return data;
}

// kind: 'classes' | 'subclasses'
async function buildFeatureList(kind, index) {
  const levels = await getJSON(`${API}/${kind}/${index}/levels`);
  const out = [];
  for (const lvl of levels) {
    for (const f of lvl.features ?? []) {
      const detail = await getFeature(f.index);
      const desc = Array.isArray(detail.desc) ? detail.desc.join('\n') : (detail.desc ?? '');
      out.push({
        id: f.index,                       // stable id (used for dismiss-set)
        level: detail.level ?? lvl.level,  // class level at which it's gained
        name: f.name,
        desc: desc.trim(),
      });
    }
  }
  // Sort by level then name for a stable, readable file.
  out.sort((a, b) => a.level - b.level || a.name.localeCompare(b.name));
  return out;
}

async function main() {
  const classes = {};
  for (const c of CLASSES) {
    process.stderr.write(`Fetching class ${c}…\n`);
    classes[c] = await buildFeatureList('classes', c);
    process.stderr.write(`  ${classes[c].length} features\n`);
  }

  const subclasses = {};
  for (const s of SUBCLASSES) {
    process.stderr.write(`Fetching subclass ${s}…\n`);
    subclasses[s] = await buildFeatureList('subclasses', s);
    process.stderr.write(`  ${subclasses[s].length} features\n`);
  }

  const banner =
    '// AUTO-GENERATED by scripts/fetch-class-features.mjs — do not edit by hand.\n' +
    '// Source: dnd5e API (https://www.dnd5eapi.co) — SRD base-class + subclass features by level.\n' +
    '// Tasha\'s/Xanathar\'s content is hand-authored in homebrew-content.js (not here).\n' +
    '// Re-run: node scripts/fetch-class-features.mjs\n\n';
  const body =
    `const CLASS_FEATURES = ${JSON.stringify(classes, null, 2)};\n\n` +
    `const SUBCLASS_FEATURES = ${JSON.stringify(subclasses, null, 2)};\n`;

  await writeFile(OUT, banner + body, 'utf8');
  const cTotal = Object.values(classes).reduce((n, arr) => n + arr.length, 0);
  const sTotal = Object.values(subclasses).reduce((n, arr) => n + arr.length, 0);
  process.stderr.write(
    `\nWrote ${OUT} — ${CLASSES.length} classes (${cTotal} features), ` +
    `${SUBCLASSES.length} subclasses (${sTotal} features).\n`);
}

main().catch((err) => {
  process.stderr.write(`ERROR: ${err.message}\n`);
  process.exit(1);
});
